import math
a = float(input('Digite um 창ngulo: '))
seno = math.sin(math.radians(a))
print('O 창ngulo {} tem o SENO de {:.2f}'.format(a, seno))
cosseno = math.cos(math.radians(a))
print('O 창ngulo {} tem o COSSENO de {:.2f}'.format(a, cosseno))
tan = math.tan(math.radians(a))
print('O 창ngulo {} tem a TANGENTE de {:.2f}'.format(a, tan))

